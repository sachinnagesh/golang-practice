package gobyexample

import "fmt"

func VariablesExample() {

	var a int = 1
	fmt.Println(a)

	var b, c, d int = 2, 3, 4
	fmt.Println(b, c, d)

	var e, f = 5, 6
	fmt.Println(e, f)

	//default value
	var no int
	fmt.Println(no)

	var name string
	fmt.Println(name)

	var flag bool
	fmt.Println(flag)

}
